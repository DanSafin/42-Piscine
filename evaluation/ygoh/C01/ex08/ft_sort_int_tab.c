void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	temp;
	int	last;

	i = 0;
	last = size - 1;
	while (i < last)
	{
		if (tab[i] > tab[i + 1])
		{
			temp = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = temp;
			i = 0;
		}
		else
			i++;
	}
}
