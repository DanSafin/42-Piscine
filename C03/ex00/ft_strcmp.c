int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	result;

	result = 0;
	i = 0;
	while (s1[i] == s2[i] && s1[i])
	{
		i++;
	}
	result = s1[i] - s2[i];
	return (result);
}
