int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	result;
	int	j;

	i = 0;
	j = 0;
	sign = 1;
	result = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			j++;
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + str[i] - '0';
		i++;
	}
	if (j % 2 != 0)
		sign = -1;
	return (result * sign);
}

